void	ft_sort_int_tab(int *tab, int size)
{
	int	count;
	int	i;
	int	temp;

	count = 0;
	i = 0;
	while (count < size)
	{
		while (i < size)
		{
			if (tab[i] > tab[i + 1])
			{
				temp = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = temp;
			}
			i++;
		}
		count++;
		i = 0;
	}
}
